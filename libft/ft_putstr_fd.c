#include <unistd.h>

int	ft_putstr_fd(char *s, int fd)
{
	int	i;

	i = 0;
	if (!s)
		return (write(fd, "(null)", 6));
	while (s && *s)
	{
		i += write(fd, s, 1);
		s++;
	}
	return (i);
}
