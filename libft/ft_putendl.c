#include <unistd.h>

int	ft_putendl(char *s)
{
	int	i;

	i = 0;
	if (!s)
		return (write(1, "(null)\n", 7));
	while (*s)
	{
		write(1, s++, 1);
		i++;
	}
	write(1, "\n", 1);
	return (i + 1);
}
